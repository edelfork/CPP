template<typename T>
bool easyfind(T container, int n)
{
    typename T::iterator it = std::find(container.begin(), container.end(), n);
    std::cout << "Find " << n << ": " << *it << " (equals = true , 0 = false)" << std::endl;
    if (it == container.end())
        return (false);
    return (true);
}

